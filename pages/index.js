export async function getServerSideProps() {
  const API_KEY = process.env.API_KEY;
  const { results } = await (
    await fetch(`https://api.themoviedb.org/3/movie/popular?api_key=${API_KEY}`)
  ).json();
  return {
    props: {
      results,
    },
  };
}
